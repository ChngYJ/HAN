class MovingAvg:
    """
    Moving average block to highlight the trend of time series
    """

    def __init__(self, kernel_size, stride):
        self.kernel_size = kernel_size
        self.stride = stride

    def forward(self, x):
        # Padding on both ends of time series
        front = np.repeat(x[:1, :], (self.kernel_size - 1) // 2, axis=0)
        end = np.repeat(x[-1:, :], (self.kernel_size - 1) // 2, axis=0)
        x = np.concatenate([front, x, end], axis=0)
        # Perform 1D average pooling using convolution
        kernel = np.ones(self.kernel_size) / self.kernel_size
        x = np.apply_along_axis(lambda m: np.convolve(m, kernel, mode='valid'), axis=0, arr=x.squeeze())

        return x


class SeriesDecomp:
    """
    Series decomposition block
    """

    def __init__(self, kernel_size):
        self.moving_avg = MovingAvg(kernel_size, stride=1)

    def forward(self, x):
        moving_mean = self.moving_avg.forward(x)
        res = x - moving_mean
        return res, moving_mean


def cluster_channel(data, factor, moving_avg, num_clusters):
    batch_x = data[:500, :]
    means = np.mean(batch_x, axis=0, keepdims=True)  # temporal
    batch_x = batch_x - means
    stdev = np.sqrt(np.var(batch_x, axis=0, keepdims=True) + 1e-5)
    batch_x = batch_x / stdev
    res, trend = SeriesDecomp(moving_avg).forward(batch_x)
    correlation_matrix = stats.spearmanr(trend)[0]
    kmeans = KMeans(n_clusters=num_clusters, random_state=144)
    cluster_labels = kmeans.fit_predict(correlation_matrix)
    cluster_dict = {i: [] for i in range(num_clusters)}
    for i, label in enumerate(cluster_labels):
        cluster_dict[label].append(i)

    reordered_channels = [data[:, idx] for idx_list in cluster_dict.values() for idx in idx_list]
    reordered_data = np.column_stack(reordered_channels)

    return cluster_dict, reordered_data


class Dataset_ETT_hour(Dataset):
    def __init__(self, root_path, flag='train', size=None,
                 features='S', data_path='ETTh1.csv',
                 target='OT', scale=True, timeenc=0, freq='h', seasonal_patterns=None, factor=3, moving_avg=25, num_clusters=1):
        # size [seq_len, label_len, pred_len]
        # info
        if size == None:
            self.seq_len = 24 * 4 * 4
            self.label_len = 24 * 4
            self.pred_len = 24 * 4
        else:
            self.seq_len = size[0]
            self.label_len = size[1]
            self.pred_len = size[2]
        # init
        assert flag in ['train', 'test', 'val']
        type_map = {'train': 0, 'val': 1, 'test': 2}
        self.set_type = type_map[flag]

        self.features = features
        self.target = target
        self.scale = scale
        self.timeenc = timeenc
        self.freq = freq
        self.flag = flag
        self.cluster = True
        self.factor = factor
        self.cluster_channel = cluster_channel
        self.moving_avg = moving_avg
        self.num_clusters = num_clusters

        self.root_path = root_path
        self.data_path = data_path
        self.__read_data__()

    def __read_data__(self):
        self.scaler = StandardScaler()
        df_raw = pd.read_csv(os.path.join(self.root_path,
                                          self.data_path))

        border1s = [0, 12 * 30 * 24 - self.seq_len, 12 * 30 * 24 + 4 * 30 * 24 - self.seq_len]
        border2s = [12 * 30 * 24, 12 * 30 * 24 + 4 * 30 * 24, 12 * 30 * 24 + 8 * 30 * 24]
        border1 = border1s[self.set_type]
        border2 = border2s[self.set_type]

        if self.features == 'M' or self.features == 'MS':
            cols_data = df_raw.columns[1:]
            df_data = df_raw[cols_data]
        elif self.features == 'S':
            df_data = df_raw[[self.target]]

        if self.scale:
            train_data = df_data[border1s[0]:border2s[0]]
            self.scaler.fit(train_data.values)
            data = self.scaler.transform(df_data.values)
        else:
            data = df_data.values

        if self.cluster:
            # data (all_T, C)
            self.cluster_dict, data = self.cluster_channel(data, self.factor, self.moving_avg, self.num_clusters)

        df_stamp = df_raw[['date']][border1:border2]
        df_stamp['date'] = pd.to_datetime(df_stamp.date)
        if self.timeenc == 0:
            df_stamp['month'] = df_stamp.date.apply(lambda row: row.month, 1)
            df_stamp['day'] = df_stamp.date.apply(lambda row: row.day, 1)
            df_stamp['weekday'] = df_stamp.date.apply(lambda row: row.weekday(), 1)
            df_stamp['hour'] = df_stamp.date.apply(lambda row: row.hour, 1)
            data_stamp = df_stamp.drop(['date'], 1).values
        elif self.timeenc == 1:
            data_stamp = time_features(pd.to_datetime(df_stamp['date'].values), freq=self.freq)
            data_stamp = data_stamp.transpose(1, 0) 

        self.data_x = data[border1:border2]
        self.data_y = data[border1:border2]
        self.data_stamp = data_stamp

    def __getitem__(self, index):
        s_begin = index
        s_end = s_begin + self.seq_len
        r_begin = s_end - self.label_len
        r_end = r_begin + self.label_len + self.pred_len

        seq_x = self.data_x[s_begin:s_end]
        seq_y = self.data_y[r_begin:r_end]
        # if self.flag == 'test':
        #     seq_x_mark = self.data_stamp[s_begin:r_end]  # 最后堆叠
        # else:
        #     seq_x_mark = self.data_stamp[s_begin:s_end]
        seq_x_mark = self.data_stamp[s_begin:s_end]
        seq_y_mark = self.data_stamp[r_begin:r_end]

        return seq_x, seq_y, seq_x_mark, seq_y_mark

    def __len__(self):
        return len(self.data_x) - self.seq_len - self.pred_len + 1

    def inverse_transform(self, data):
        return self.scaler.inverse_transform(data)


